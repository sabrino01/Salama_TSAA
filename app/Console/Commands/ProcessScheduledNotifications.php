<?php

namespace App\Console\Commands;

use App\Jobs\SendScheduledNotifications;
use Illuminate\Console\Command;

class ProcessScheduledNotifications extends Command
{
    /**
     * The name and signature of the console command.
     *
     * @var string
     */
    protected $signature = 'notifications:process';

    /**
     * The console command description.
     *
     * @var string
     */
    protected $description = 'Process and send scheduled frequency notifications';

    /**
     * Create a new command instance.
     *
     * @return void
     */
    public function __construct()
    {
        parent::__construct();
    }

    /**
     * Execute the console command.
     *
     * @return int
     */
    public function handle()
    {
        $this->info('Processing scheduled notifications...');

        SendScheduledNotifications::dispatch();

        $this->info('Scheduled notifications job dispatched.');

        return 0;
    }
}
